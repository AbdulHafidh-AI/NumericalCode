public class angle {
    public static void main(String[] args) {
        double x = 30;
        double y = 90;

        // Nilai sin
        x =  Math.toRadians(x);
        System.out.println(x);

        y =   Math.toRadians(y);
        System.out.println(y);

        // Nilai cos
        System.out.println(Math.cos(x));
        System.out.println(Math.cos(y));
        

    }
}
